function criarRegrasDaMissao(missao) {
    const texto = (missao.descricao + " " + missao.objetivo).toLowerCase();

    const regras = [];

    if (missao.linguagem === "HTML") {
        if (texto.includes("estrutura básica")) regras.push("html", "head", "body");
        if (texto.includes("h1")) regras.push("h1");
        if (texto.includes("parágrafo") || texto.includes("p.")) regras.push("p");
        if (texto.includes("lista não ordenada")) regras.push("ul", "li");
        if (texto.includes("lista ordenada")) regras.push("ol", "li");
        if (texto.includes("imagem")) regras.push("img", "alt");
        if (texto.includes("link")) regras.push("a", "href");
        if (texto.includes("tabela")) regras.push("table", "tr", "td");
        if (texto.includes("formulário")) regras.push("form", "input", "button");
        if (texto.includes("header")) regras.push("header");
        if (texto.includes("main")) regras.push("main");
        if (texto.includes("footer")) regras.push("footer");
        if (texto.includes("nav")) regras.push("nav");
        if (texto.includes("section")) regras.push("section");
        if (texto.includes("article")) regras.push("article");
        if (texto.includes("iframe")) regras.push("iframe");
        if (texto.includes("details")) regras.push("details", "summary");
    }

    if (missao.linguagem === "CSS") {
        if (texto.includes("cor")) regras.push("color");
        if (texto.includes("fonte")) regras.push("font");
        if (texto.includes("centralização")) regras.push("text-align");
        if (texto.includes("background")) regras.push("background");
        if (texto.includes("padding")) regras.push("padding");
        if (texto.includes("border-radius")) regras.push("border-radius");
        if (texto.includes("cursor")) regras.push("cursor");
        if (texto.includes("borda")) regras.push("border");
        if (texto.includes("margin")) regras.push("margin");
        if (texto.includes("hover")) regras.push(":hover");
        if (texto.includes("box-shadow")) regras.push("box-shadow");
        if (texto.includes("flexbox")) regras.push("display", "flex");
        if (texto.includes("grid")) regras.push("display", "grid");
        if (texto.includes("transition")) regras.push("transition");
        if (texto.includes("transform")) regras.push("transform");
        if (texto.includes("media")) regras.push("@media");
        if (texto.includes("variáveis")) regras.push(":root", "var");
        if (texto.includes("keyframes")) regras.push("@keyframes");
    }

    if (missao.linguagem === "JavaScript") {
        if (texto.includes("console")) regras.push("console.log");
        if (texto.includes("variável")) regras.push("let");
        if (texto.includes("soma")) regras.push("+");
        if (texto.includes("if")) regras.push("if");
        if (texto.includes("else")) regras.push("else");
        if (texto.includes("função")) regras.push("function");
        if (texto.includes("array")) regras.push("[", "]");
        if (texto.includes("for")) regras.push("for");
        if (texto.includes("while")) regras.push("while");
        if (texto.includes("objeto")) regras.push("{", "}");
        if (texto.includes("localstorage")) regras.push("localStorage");
        if (texto.includes("filtro")) regras.push("filter");
        if (texto.includes("createelement")) regras.push("createElement");
        if (texto.includes("confirmação")) regras.push("confirm");
    }

    if (missao.linguagem === "Python") {
        if (texto.includes("mensagem")) regras.push("print");
        if (texto.includes("nome") || texto.includes("usuário")) regras.push("input");
        if (texto.includes("soma")) regras.push("+");
        if (texto.includes("média")) regras.push("/");
        if (texto.includes("if")) regras.push("if");
        if (texto.includes("else")) regras.push("else");
        if (texto.includes("par ou ímpar")) regras.push("%");
        if (texto.includes("for")) regras.push("for");
        if (texto.includes("while")) regras.push("while");
        if (texto.includes("lista")) regras.push("[", "]");
        if (texto.includes("função")) regras.push("def");
        if (texto.includes("dicionário")) regras.push("{", "}");
        if (texto.includes("try")) regras.push("try", "except");
        if (texto.includes("arquivo")) regras.push("open");
    }

    if (missao.linguagem === "Inglês") {
        regras.push("english");
    }

    return regras;
}

